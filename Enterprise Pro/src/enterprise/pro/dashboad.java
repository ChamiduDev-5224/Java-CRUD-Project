    private void btnResetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnResetActionPerformed
        //Password Reset    
        String un,oldpw,newpw,conpw;
              
              un = txtUname.getText();
              oldpw = txtoldPW.getText();
              newpw = txtNWpw.getText();
              conpw = txtCONpw.getText();
              
              String query = "SELECT username, password FROM login WHERE username='"+un+"' ";
              String upQuery = "UPDATE login SET password='"+newpw+"' WHERE username='"+un+"'";
                try{
                
            con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/eterprise_db" , "root" , "");
            pst = con .prepareStatement(query);

            ResultSet rs = pst.executeQuery(query);

                    if(rs.next()){
                    
                        if(rs.getString("password").equals(oldpw)){
                        
                        if(newpw.equals(conpw)){
                            
                            pst.executeUpdate(upQuery);
                            JOptionPane.showMessageDialog(null, "Password changed");
                            txtUname.setText("");
                            txtoldPW.setText("");
                            txtNWpw.setText("");
                            txtCONpw.setText("");
                            
                        }else {
                        
                            JOptionPane.showMessageDialog(null, "Password Not Match");
                            txtCONpw.requestFocus();
                        
                        }
                        }
                        else{
                        
                               JOptionPane.showMessageDialog(null, "invalid old Password");
                               txtoldPW.requestFocus();
                        }
                    }
                    else{
                    
                            JOptionPane.showMessageDialog(null, "invalid username");
                            txtUname.requestFocus();
                    }
                }catch(Exception e){
                    JOptionPane.showMessageDialog(null, e);
                }
                
              
    }//GEN-LAST:event_btnResetActionPerformed

