    private void searchBarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchBarKeyReleased
        //data searching in update page
        try{
            String query = "SELECT * FROM cusdetail WHERE nic =?";

            con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/eterprise_db" , "root" , "");
            pst = con .prepareStatement(query);
            pst.setString(1,searchBar.getText());
            ResultSet rs = pst.executeQuery();

            if(rs.next()){

                String fn2 = rs. getString("fname");
                t21.setText(fn2);

                String ln2 = rs. getString("lastname");
                t22.setText(ln2);

                String nic2 = rs. getString("nic");
                t23.setText(nic2);

                String addre2 = rs. getString("address");
                t24.setText(addre2);

                //radio button searching
                String sex = rs .getString("gender");
                if (sex.equals("Male")){
                    r21.setSelected(true);
                } else {
                    r22.setSelected(true);
                }
                //combo box value seaching
                String conType = rs.getString("connectiontype");
                switch(conType) {

                    case "Mobile" :
                    c21.setSelectedIndex(0);
                    break;
                    case "Home BroadBrand" :
                    c21.setSelectedIndex(1);
                    break;

                    case "Television" :
                    c21.setSelectedIndex(2);
                    break;

                }

                String mobile2 = rs. getString("mobile");
                t25.setText(mobile2);

                String  email2 = rs.getString("email");
                t26.setText(email2);

            }

        }catch (Exception e){

            JOptionPane.showMessageDialog(null, e);

        }
        
    }//GEN-LAST:event_searchBarKeyReleased

